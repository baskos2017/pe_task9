"""В цьому завданні використовується код з завдання 3 сьомого уроку цього /
курсу. Посто розбито на два файли в одному з яких виконано імпорт  іншого як /
модуля  та імпорт імені функції генератора gen_primes"""



def gen_primes(n, i = 2, prim = []):
    
    while n:
        if all(i%d for d in prim):
            n -= 1
            prim.append(i)
            yield i
        i += 1 + i%2